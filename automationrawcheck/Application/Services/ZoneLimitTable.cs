namespace AutomationRawCheck.Application.Services;

/// <summary>
/// 용도지역별 법정 건폐율(BCR)·용적률(FAR) 상한을 반환합니다.
/// </summary>
public static class ZoneLimitTable
{
    // key: 용도지역명 (부분 매칭을 위해 Contains 사용)
    // value: (BCR 상한 %, FAR 상한 %)
    private static readonly (string Key, double Bcr, double Far)[] _limits =
    {
        // ── 주거지역 ────────────────────────────────────────────────
        ("제1종전용주거지역",  50,  100),
        ("제2종전용주거지역",  50,  150),
        ("제1종일반주거지역",  60,  200),
        ("제2종일반주거지역",  60,  250),
        ("제3종일반주거지역",  50,  300),
        ("준주거지역",         70,  500),
        // ── 상업지역 ────────────────────────────────────────────────
        ("중심상업지역",       90, 1500),
        ("일반상업지역",       80, 1300),
        ("근린상업지역",       70,  900),
        ("유통상업지역",       80, 1100),
        // ── 공업지역 ────────────────────────────────────────────────
        ("전용공업지역",       70,  300),
        ("일반공업지역",       70,  350),
        ("준공업지역",         70,  400),
        // ── 녹지지역 ────────────────────────────────────────────────
        ("보전녹지지역",       20,   80),
        ("생산녹지지역",       20,  100),
        ("자연녹지지역",       20,  100),
    };

    /// <summary>
    /// 용도지역명으로 법정 BCR/FAR 상한을 조회합니다.
    /// 미매칭이면 null을 반환합니다.
    /// </summary>
    public static (double Bcr, double Far)? GetLimit(string? zoneName)
    {
        if (string.IsNullOrWhiteSpace(zoneName)) return null;

        foreach (var (key, bcr, far) in _limits)
        {
            if (zoneName.Contains(key, StringComparison.OrdinalIgnoreCase))
                return (bcr, far);
        }
        return null;
    }
}
